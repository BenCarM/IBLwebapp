    <h1>
        You have Oily Skin!
    </h1>
    <p class="topic">
        Definition of oily skin <br> </p>
        <p>
            Oily skin is a skin type characterized by excess sebum (oil) production by the sebaceous glands. <br> This overproduction often leads to a shiny appearance, enlarged pores, and a higher likelihood of acne and blackheads
        </p><br>
    </p>
    <p class="topic">
        Your skin charactersistics also include;<br> </p>
        <p>
        Shiny Appearance: Especially noticeable on the forehead, nose, and chin (T-zone). <br>
        Enlarged Pores: Particularly around the nose and cheeks. <br>
        Frequent Breakouts: Including acne, blackheads, and whiteheads due to clogged pores. <br>
        Thicker Skin Texture: The skin may feel somewhat thick or coarse. <br>
        Less Visible Wrinkles: Due to the natural lubrication, oily skin often shows fewer signs of aging, such as fine lines and wrinkles. <br>
      
    </p>
   
        <p class="topic"> What to avoid:<br></p>
         <p>
            Harsh Cleansers: Using strong cleansers can strip the skin of its natural oils, prompting even more oil production.<br>
            Heavy, Oil-Based Products: These can clog pores and exacerbate oiliness.<br>
            Over-Exfoliation: Too much exfoliation can irritate the skin and trigger increased oil production.<br>
            Skipping Moisturizer: Contrary to popular belief, oily skin still needs hydration. Opt for lightweight, non-comedogenic moisturizers.<br>
          Alcohol-Based Products: These can dry out the skin, leading to more oil production as the skin tries to compensate.<br>
          
    </p> <br>
