        <h1>
        You have Dry Skin!
    </h1>
    <p class="topic">
        Definition of dry skin <br> </p>
        <p>
            This is a skin type that produces <span>less sebum</span> than normal skin. It does not have lipids 
            to form a shield to protect the skin from external pollutants.The lipids are also used to retain moisture,
            dry skin lacks this. Dry skin <span>loses moisture quickly</span>.
        </p><br>
        <p class="topic"> What does dry Skin look like?<br></p>
         <p>&#x2022 <span>Dull</span> <br>
            &#x2022 Sometimes <span>flaky</span> or <span>scaly</span><br>
            &#x2022 <span>Rough</span><br>
            &#x2022 More appearance of <span>fine lines</span> and<span> wrinkles</span><br> <br>

    </p>
    <p class="topic">
        What does dry Skin feel like?<br>
    </p>
    <p>
        &#x2022 <span>Tight</span><br>
        &#x2022 <span>Itchy</span><br>
        &#x2022 <span>Uncomfortable</span><br>
    </p>
    <p class="topic">
        What to avoid
    </p>
    <p>
        If you have dry skin, it's essential to avoid certain products and habits that can exacerbate dryness and lead to irritation or discomfort. Here’s what to avoid:
<p class="small-topic">1. Harsh Cleansers:</p>
   <p><b>Avoid</b> cleansers with sulfates and high alcohol content, as they can strip your skin of its natural oils.</p>
<p class="small-topic">2. Alcohol-Based Products:</p>
    <p><b>Avoid</b> products with high alcohol content, such as certain toners or astringents, as they can dry the skin.</p>
    </p>
    <br><br><br><br>
    <p class="topic">Skincare Routine for Dry Skin</p>

    <p class="small-topic">Morning Routine:</p>
    <p style="font-family: Arial, Helvetica, sans-serif;"><ul>
        <li><strong>Cleansing:</strong>
            <ul>
                <li>Use a Gentle Cleanser: Opt for a hydrating or creamy cleanser that doesn’t strip natural oils. Look for products labeled “sulfate-free” or “for dry skin.”</li>
                <li>Example: <a href="https://www.cerave.com/skincare/cleansers/hydrating-facial-cleanser">CeraVe hydrating-facial-cleanser</a></li>
            </ul>
        </li>
        <li><strong>Toning:</strong>
            <ul>
                <li>Choose a Hydrating Toner: Use a toner that contains moisturizing ingredients like hyaluronic acid or glycerin, and avoid those with alcohol.</li>
                <li>Example: <a href="https://www.klairscosmetics.com/product/supple-preparation-unscented-toner/">Klairs Supple Preparation Unscented Toner</a> </li>
            </ul>
        </li>
        <li><strong>Serum:</strong>
            <ul>
                <li>Apply a Hydrating Serum: Look for serums with hyaluronic acid, glycerin, or squalane to boost moisture levels.</li>
                <li>Example: The Ordinary Hyaluronic Acid 2% + B5, Vichy Mineral 89.</li>
            </ul>
        </li>
        <li><strong>Moisturizing:</strong>
            <ul>
                <li>Use a Rich Moisturizer: Apply a thick, emollient moisturizer to lock in hydration. Look for ingredients like ceramides, shea butter, or squalane.</li>
                <li>Example: CeraVe Moisturizing Cream, Eucerin Advanced Repair Cream.</li>
            </ul>
        </li>
        <li><strong>Sun Protection:</strong>
            <ul>
                <li>Apply Sunscreen: Use a broad-spectrum SPF 30 or higher. Look for hydrating formulations that won’t dry out the skin.</li>
                <li>Example: La Roche-Posay Anthelios Melt-in Milk Sunscreen, EltaMD UV Clear Broad-Spectrum SPF 46.</li>
            </ul>
        </li>
    </ul>

    <p class="small-topic">Evening Routine:</h2></p>
    <ul>
        <li><strong>Cleansing:</strong>
            <ul>
                <li>Use a Gentle Cleanser: Cleanse to remove makeup and impurities. A creamy or hydrating cleanser works well.</li>
            </ul>
        </li>
        <li><strong>Toning:</strong>
            <ul>
                <li>Apply a Hydrating Toner: Similar to your morning routine, use a toner to prepare your skin for the next steps.</li>
            </ul>
        </li>
        <li><strong>Serum:</strong>
            <ul>
                <li>Apply a Nourishing Serum: Use a serum with hydrating or reparative ingredients, like peptides or niacinamide, to support skin repair overnight.</li>
                <li>Example: The Ordinary Buffet, Olay Regenerist Retinol 24.</li>
            </ul>
        </li>
        <li><strong>Moisturizing:</strong>
            <ul>
                <li>Use a Rich Night Cream: Opt for a thicker moisturizer or night cream to provide intense hydration while you sleep.</li>
                <li>Example: Neutrogena Hydro Boost Water Gel, Dr. Jart+ Ceramidin Cream.</li>
            </ul>
        </li>
        <li><strong>Face Masks:</strong>
            <ul>
                <li>Apply Hydrating Masks: Use a hydrating face mask once or twice a week to give your skin an extra moisture boost.</li>
                <li>Example: Laneige Water Sleeping Mask, Origins Drink Up Intensive Overnight Mask.</li>
            </ul>
        </li>
    </ul>
    </p>
