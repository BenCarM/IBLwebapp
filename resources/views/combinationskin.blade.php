        <h1>You have Combination Skin!</h1>
        <h2>Definition of Combination Skin</h2>
        <p>
        Combination skin, also known as known as normal skin type, is neither too dry nor too too oily.It has a regular texture, no imperfections, and a clean,soft appearance, and normally does not need special skincare.
        </p>
        <h2>Characteristics of Combination Skin</h2>
        <p>
            <ul>
                <li>A clear appearance and even colour</li>
                <li>Soft and supple skin when touched.</li>
                <li>Has small, less visible pores.</li>
                <li>Not sensitive to normal external stimulation.</li>
                <li>Feels neither tight nor greasy especially after cleansing.</li>
                <li>Has good elasticity and moisture.</li>
            </ul>
        </p>
